package com.example.learnrecyclerview;

public class ListOfNotes {

    private String h;
    private String d;

    public ListOfNotes(String h, String d) {
        this.h = h;
        this.d = d;
    }

    public String getH() {
        return h;
    }

    public String getD() {
        return d;
    }
}
